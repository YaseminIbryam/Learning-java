package Notes;

public class print {
    public static void main(String[] args) {
        System.out.println("Hello world!"); // print something and goes to the next line
        // shortcut - sout


        System.out.printf("%s, %d, %f%n", "hello", 15, 1.0); // print and format, doesn't go to the next line(we have to put %n to do it)
        //shortcut - souf

        System.out.print("Hello word!"); // just print and stay in the same line
    }
}
